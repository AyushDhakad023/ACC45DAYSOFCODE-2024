#include <stdio.h>

int main() {
    int grade=60;
    if(grade>=90) {
        printf("grade:A.\n");
    }else if (grade>=80){
        printf("grade:B.\n");
    }else if(grade>=70){
        printf("grade:C.\n");
    }else{
        printf("grade:F\n");
        
    }
    return 0;
    
}
