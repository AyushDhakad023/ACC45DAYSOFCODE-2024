#include <stdio.h>

int main() {
    int num1 = 15; // First number
    int num2 = 25; // Second number
    int sum;

    // Heading
    printf("=== Sum of Two Numbers ===\n\n");

    // Calculate the sum
    sum = num1 + num2;

    // Print the results
    printf("First number: %d\n", num1);
    printf("Second number: %d\n", num2);
    printf("Sum: %d\n", sum);

    return 0;
}
